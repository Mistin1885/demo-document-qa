"""Unit tests for ``app.vespa.app_package``.

All tests are deterministic and have NO external dependencies (no Vespa,
no network, no sleep).

Coverage:
- ``build_application_package(dim)`` returns a valid ``ApplicationPackage``
  with the correct schema name, all required fields, and all 5 rank profiles.
- The embedding tensor type string reflects the supplied ``embedding_dim``.
- All 14 ``source_type`` values are covered by the boost map (or have a
  default fallback of 1.0 built into the expression).
- ``write_application_files(tmp_path, dim)`` creates ``schemas/document_chunk.sd``
  on disk containing ``tensor<float>(x[DIM])``.
- ``services.xml`` generated by ``write_application_files`` contains the
  correct container id (``default``) and content cluster id (``documents``).
- ``hosts.xml`` contains the ``node1`` alias.
- ``validation-overrides.xml`` contains the required validation IDs.
"""

from __future__ import annotations

import pytest

from app.vespa.app_package import (
    SOURCE_TYPE_BOOST,
    SOURCE_TYPES,
    build_application_package,
    write_application_files,
)

# ---------------------------------------------------------------------------
# Fixtures
# ---------------------------------------------------------------------------

DEFAULT_DIM = 1024


@pytest.fixture(scope="module")
def pkg_1024():
    """Build once; reused across tests in this module."""
    return build_application_package(DEFAULT_DIM)


@pytest.fixture(scope="module")
def schema_1024(pkg_1024):
    return pkg_1024.schema


