def test_write_application_files_creates_sd(tmp_path):
    """``write_application_files`` must create a ``document_chunk.sd`` file."""
    write_application_files(tmp_path, embedding_dim=1024)
    sd = tmp_path / "schemas" / "document_chunk.sd"
    assert sd.exists(), "schemas/document_chunk.sd must exist after write_application_files"


def test_sd_contains_correct_tensor_type(tmp_path):
    write_application_files(tmp_path, embedding_dim=1024)
    sd = tmp_path / "schemas" / "document_chunk.sd"
    content = sd.read_text()
    assert "tensor<float>(x[1024])" in content


def test_sd_tensor_type_changes_with_dim(tmp_path):
    d256 = tmp_path / "dim256"
    write_application_files(d256, embedding_dim=256)
    sd_content = (d256 / "schemas" / "document_chunk.sd").read_text()
    assert "tensor<float>(x[256])" in sd_content
    assert "tensor<float>(x[1024])" not in sd_content


def test_sd_contains_all_rank_profile_names(tmp_path):
    write_application_files(tmp_path, embedding_dim=1024)
    sd = tmp_path / "schemas" / "document_chunk.sd"
    content = sd.read_text()
    for name in REQUIRED_RANK_PROFILES:
        assert name in content, f"Rank profile {name!r} not found in document_chunk.sd"


def test_services_xml_created(tmp_path):
    write_application_files(tmp_path, embedding_dim=1024)
    svc = tmp_path / "services.xml"
    assert svc.exists()
    content = svc.read_text()
    assert 'id="default"' in content
    assert 'id="documents"' in content


def test_services_xml_query_timeout(tmp_path):
    write_application_files(tmp_path, embedding_dim=1024)
    content = (tmp_path / "services.xml").read_text()
    assert "5.0" in content or "5s" in content or "query-timeout" in content


def test_hosts_xml_created(tmp_path):
    write_application_files(tmp_path, embedding_dim=1024)
    hosts = tmp_path / "hosts.xml"
    assert hosts.exists()
    content = hosts.read_text()
    assert "node1" in content
    assert "localhost" in content


def test_validation_overrides_xml_created(tmp_path):
    write_application_files(tmp_path, embedding_dim=1024, validation_until="2026-12-31")
    vo = tmp_path / "validation-overrides.xml"
    assert vo.exists()
    content = vo.read_text()
    assert "tensor-type-change" in content
    assert "field-type-change" in content
    assert "indexing-change" in content
    assert "2026-12-31" in content


