# ---------------------------------------------------------------------------
# Embedding tensor type
# ---------------------------------------------------------------------------


def test_embedding_tensor_type_default(schema_1024):
    emb_field = next(f for f in schema_1024.document.fields if f.name == "embedding")
    assert emb_field.type == "tensor<float>(x[1024])"


def test_embedding_tensor_type_custom_dim():
    pkg_512 = build_application_package(512)
    emb_field = next(
        f for f in pkg_512.schema.document.fields if f.name == "embedding"
    )
    assert emb_field.type == "tensor<float>(x[512])"
    # Changing dim changes the type string
    pkg_768 = build_application_package(768)
    emb_field_768 = next(
        f for f in pkg_768.schema.document.fields if f.name == "embedding"
    )
    assert emb_field_768.type == "tensor<float>(x[768])"


# ---------------------------------------------------------------------------
# Rank profiles
# ---------------------------------------------------------------------------

REQUIRED_RANK_PROFILES = [
    "bm25_only",
    "semantic_only",
    "hybrid_first_phase",
    "hybrid_with_native_rerank",
    "hybrid_for_cross_encoder",
]


def test_all_rank_profiles_present(schema_1024):
    profile_names = list(schema_1024.rank_profiles.keys())
    for name in REQUIRED_RANK_PROFILES:
        assert name in profile_names, f"Missing rank profile: {name!r}"


def test_bm25_only_has_no_vector_first_phase(schema_1024):
    rp = schema_1024.rank_profiles["bm25_only"]
    # first_phase should reference bm25(…) but NOT closeness
    expr = str(rp.first_phase)
    assert "bm25" in expr
    assert "closeness" not in expr


def test_semantic_only_first_phase(schema_1024):
    rp = schema_1024.rank_profiles["semantic_only"]
    expr = str(rp.first_phase)
    assert "closeness" in expr
    # No bm25 in first_phase expression (bm25 may appear in functions only)
    assert "bm25" not in expr


def test_hybrid_profiles_have_vector_in_first_phase(schema_1024):
    for name in ("hybrid_first_phase", "hybrid_for_cross_encoder"):
        rp = schema_1024.rank_profiles[name]
        expr = str(rp.first_phase)
        assert "closeness" in expr, f"{name} first_phase should include closeness"
        assert "bm25" in expr, f"{name} first_phase should include bm25"


def test_hybrid_with_native_rerank_has_second_phase(schema_1024):
    rp = schema_1024.rank_profiles["hybrid_with_native_rerank"]
    assert rp.second_phase is not None, "hybrid_with_native_rerank must have a second_phase"


def test_hybrid_for_cross_encoder_no_second_phase(schema_1024):
    rp = schema_1024.rank_profiles["hybrid_for_cross_encoder"]
    assert rp.second_phase is None, "hybrid_for_cross_encoder must NOT have a second_phase"


def test_rank_profiles_expose_match_features(schema_1024):
    for name in REQUIRED_RANK_PROFILES:
        rp = schema_1024.rank_profiles[name]
        assert rp.match_features, f"{name} must declare match_features"
        feature_strs = [str(f) for f in rp.match_features]
        assert any("bm25" in f for f in feature_strs), (
            f"{name} match_features must include a bm25 feature"
        )
        assert any("closeness" in f for f in feature_strs), (
            f"{name} match_features must include closeness"
        )


# ---------------------------------------------------------------------------
# source_type enumeration + boost map
# ---------------------------------------------------------------------------


def test_source_types_count():
    """There must be exactly 14 source types per CLAUDE.md §5.2."""
    assert len(SOURCE_TYPES) == 14, f"Expected 14 source types, got {len(SOURCE_TYPES)}"


def test_source_type_boost_covers_all_types_or_has_default():
    """Every SOURCE_TYPE must be in SOURCE_TYPE_BOOST or the expression has a
    numeric default fallback (which it always does — the expression ends
    with ``1.0``).  Also verify every explicitly boosted type is in
    SOURCE_TYPES.
    """
    for stype in SOURCE_TYPES:
        # Either explicitly listed or the default 1.0 fallback applies.
        weight = SOURCE_TYPE_BOOST.get(stype, 1.0)
        assert isinstance(weight, (int, float)), (
            f"Boost weight for {stype!r} must be numeric, got {weight!r}"
        )
        assert weight > 0, f"Boost weight for {stype!r} must be positive"


def test_source_type_boost_expression_contains_all_boost_entries(schema_1024):
    """The ``source_type_boost`` function in hybrid_first_phase must reference
    every key in ``SOURCE_TYPE_BOOST``."""
    rp = schema_1024.rank_profiles["hybrid_first_phase"]
    fn_map = {fn.name: fn for fn in (rp.functions or [])}
    assert "source_type_boost" in fn_map, "hybrid_first_phase must have source_type_boost function"
    expr = str(fn_map["source_type_boost"].expression)
    for stype in SOURCE_TYPE_BOOST:
        assert stype in expr, (
            f"source_type_boost expression missing entry for {stype!r}"
        )


# ---------------------------------------------------------------------------
# to_files / write_application_files — disk output
